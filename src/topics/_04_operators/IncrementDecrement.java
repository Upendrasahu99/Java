//Use increment and Decrement operators
package topics._04_operators;

public class IncrementDecrement {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++);//a++ first use the value than increment
        System.out.println(a);
        int b=4;
        System.out.println(++b);//++b first increment the value than use
        System.out.println(b);
        // same decrement operator work
    }

}
