//Example for how to use operators.
package topics._04_operators;

public class A_01_Operators {
    public static void main(String[] args){
        int a = 2;
        int b = 3;
        System.out.println(b+a);
        int c= 3+a;
        System.out.println(c*a);
        int d =b%a;//modulo operator remainder is 1
        System.out.println(d);
        d += 3; //+= operator for add the number in value
        System.out.println(d);
        System.out.println(5==5);//Comparison operators ==,<=,>=
        System.out.println(5==6||6==6);



    }

}
